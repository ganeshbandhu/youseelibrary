<h2>Partners</h2>
<p>The work at UC has been made possible due to Volunteering efforts from several Individuals and Organisations. Some of these organisations are mentioned below:</p>

<p >1. <a  href="http://www.absi.edu.in/" target="_blank">Aurora's Business School</a>: students andfaculty actively volunteer thier time in UC projects in Hyderabad.</p>
<p >2. <a  href="http://www.msdf.org/" target="_blank">Michael and Susan Dell Foundation</a>: is currently supporting our project partner Indian Grameen Services in scaling up the results based Integrated Development project at Indore. In May 2011, UC also received a grant from MSDF to support UC's work in "Establishing Standards of Reporting In Indian Grant Making Through a project Aimed at Transforming Lives of Excluded Families".</p>
<p >3. <a  href="http://olivecryptosystems.com/" target="_blank">Olive Cryto Systems LLP</a>: is providing technical supportin setting up thePayment Gateway for UC to receive online donations.</p>
<p >4. <a  href="http://www.saiseva.co.in/" target="_blank">SAI SEVA Business Solutions Pvt Ltd</a>: is a Rural BPO which provides information processing and reporting services for UC.</p>
<p >5.<a href="http://www.swasthindia.in/" target="_blank" >Swasth India Services Pvt Ltd</a> provides low cost IT solutions in for Health Care projects supported by UC</p>
<p >6. <a href="http://www.caspian.in/" target="_blank" >Caspian Advisors Pvt Ltd</a>is an investment management and advisory services company in the social enterprise space, which has supported UC in its early stage through an interest free working capital loan.</p>

